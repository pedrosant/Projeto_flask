def verif_rota(rota):
    if rota == 'logado':
        return 'home.html'
    if rota == 'usuario':
        return 'usuario.html'
    if rota == 'passageiro':
        return 'passageiro.html'
    if rota == 'motorista':
        return 'motorista.html'
    if rota == 'veiculos':
        return 'veiculos.html'    
